package com.kh.constant;

public class Constant {
	// 상수
	// = 변하지 않는 값
	public static void main(String[] args) {
		//정수
		System.out.println(7);
		System.out.println(-7); // Java는 -의 정수도 출력할 수 있다.
		// 실수(소수점이 포함된 값)
		System.out.println("-------");
		System.out.println(7.7);
		System.out.println("-------");
		// (단일)문자, 문자열
		System.out.println('돈');
		System.out.println("돈 100억");
		//논리값(true, false)
		System.out.println(true);
		System.out.println(false);
		
		System.out.println("-------");
		System.out.println(7);
		System.out.println('7');
		System.out.println("7");
	}
}
